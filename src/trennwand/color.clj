(defn day-of-week
  ([date colors]
   (case (dt/day-of-week date)
     1 (get colors 1 red-5)
     2 (get colors 2 orange-5)
     3 (get colors 3 yelow-5)
     4 (get colors 4 green-5)
     5 (get colors 5 blue-5)
     6 (get colors 6 purple-5)
     7 (get colors 7 magenta-5)))
  ([date]
    (day-of-week date {})))


(defn day-of-week-dark
  ([date colors]
   (case (dt/day-of-week date)
     1 (get colors 1 red-7)
     2 (get colors 2 orange-7)
     3 (get colors 3 yelow-7)
     4 (get colors 4 green-7)
     5 (get colors 5 blue-7)
     6 (get colors 6 purple-7)
     7 (get colors 7 magenta-7)))
  ([date]
   (day-of-week-dark date {})))